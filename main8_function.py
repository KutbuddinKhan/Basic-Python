# Function

def average(num1, num2):
    return (num1 + num2) / 2

def addition(num1, num2):
    return num1 + num2

def subtraction(num1, num2):
    return num1 - num2

def Multiplication(num1, num2):
    return num1 - num2

def division(num1, num2):
    return num1 - num2

print(average(20, 4))
print(addition(20, 4))
print(Multiplication(20, 4))
print(subtraction(20, 4))
print(division(20, 4))
